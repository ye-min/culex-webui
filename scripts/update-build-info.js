const fs = require('fs');
const path = require('path');

const now = new Date();
const year = now.getFullYear();
const month = String(now.getMonth() + 1).padStart(2, '0');
const day = String(now.getDate()).padStart(2, '0');
const hours = String(now.getHours()).padStart(2, '0');
const minutes = String(now.getMinutes()).padStart(2, '0');

const timestamp = `${year}.${month}.${day}.${hours}${minutes}`;

const content = `// This file is auto-generated by scripts/update-build-info.js
export const buildTimestamp = '${timestamp}';
`;

const targetPath = path.join(__dirname, '..', 'src', 'app', 'build-info.ts');

try {
  // Ensure directory exists (basic check)
  const dir = path.dirname(targetPath);
  if (!fs.existsSync(dir)) {
    fs.mkdirSync(dir, { recursive: true });
  }
  
  fs.writeFileSync(targetPath, content);
  console.log(`[Build Info] Successfully updated build timestamp: ${timestamp}`);
} catch (error) {
  console.error('[Build Info] Failed to update build timestamp:', error);
  process.exit(1);
}
